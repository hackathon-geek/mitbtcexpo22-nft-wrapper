// 4. Create contract instance
const _contract = new web3.eth.Contract(abi, contractAddress, { from: accountFrom.address });

module.exports = {
    mintNFT: function(ownerAddress, payload) {
        return new Promise(function(resolve, reject) {
            _mint = async (ownerAddress, payload) => {
                const mintTx = _contract.methods.safeMint(ownerAddress, payload);

				const signTxArgs ={
                    "to": contractAddress,
                    "data": mintTx.encodeABI(),
                    "gas": await mintTx.estimateGas()
                };
				
                const createTransaction = await web3.eth.accounts.signTransaction(signTxArgs, accountFrom.privateKey);
            
                const createReceipt = await web3.eth.sendSignedTransaction(createTransaction.rawTransaction);
                console.log(`Tx successful with hash: ${createReceipt.transactionHash}`);
                resolve(createReceipt);
            }
            _mint(ownerAddress, payload);
        });
    },
    getURIfromNFT: function(tokenId) {
        return new Promise(function(resolve, reject) {
            const data = _contract.methods.tokenURI(tokenId).call();
            resolve(data);
        })
    },
    getNFTOwnerAddress: function(tokenId) {
        return new Promise(function(resolve, reject) {
            const data = _contract.methods.ownerOf(tokenId).call();
            resolve(data);
        })
    },
    getContractOwnerAddress: function(tokenId) {
        return new Promise(function(resolve, reject) {
            const data = _contract.methods.owner().call();
            resolve(data);
        })
    }
};